public class Lecture8TernaryOperator {
    
    public static void main(String[] args) {
        
        // ternary operator format
        // condition ? true-clause : false clause
        
        int x = 5; 
        
        boolean even = (5%2 == 0) ? true : false; 
        
        System.out.println("Value of x=5 is even: " + even);
        
        
    }
    
}
